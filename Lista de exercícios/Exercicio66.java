public class Exercicio66 {
    public static void main(String[] args) {
        int i = 0; // Inicializa a variável de contagem

        // Estrutura 'faça enquanto', onde o loop continua enquanto i for menor ou igual a 30
        do {
            System.out.print(i + " "); // Mostra o número na tela
            i += 3; // Incrementa 3 a cada iteração
        } while (i <= 30); // Condição para continuar a execução do loop

        System.out.println("Acabou!"); // Exibe 'Acabou!' no final
    }
}
